// 节奏语义常量 —— 作者写 story 时唯一应当用的"调速旋钮"。
//
// 设计原则:
//   - 数值是"句末留白"(下一拍开始前的安静时间),不是这一拍的播放时长。
//     播放时长由文字本身长度决定 —— mock 阶段是估算,接 TTS 后是实测,你都不必管。
//   - 用语义级别而非裸数字,story 文件读起来像剧本批注:
//       pauseAfter: [PACE.breath, PACE.beat, PACE.dwell, PACE.beat]
//     一眼能扫出哪一句后面"要让它落一下"。
//   - 五档够用,不再细分(再细就开始过度设计了)。
//
// 校准基准:中文播音员自然语速 ~4.5 字/秒。一拍呼吸 ≈ 0.5 字时间 = 110ms;
// 一个段落转场停顿 ≈ 一个字时间 = 220ms;戏剧性停留 ≈ "1, 2" 数完 = ~1500ms。

export const PACE = {
  /** 0ms —— 紧接下一句,不留任何缝。用于把多句"焊死"成一团信息。慎用,容易塞 */
  none: 0,
  /** 200ms —— 默认值。正常断句呼吸,逗号/句号自然停顿感 */
  beat: 200,
  /** 500ms —— 短停顿。段落小转折,让上一句沉一下 */
  breath: 500,
  /** 900ms —— 长停顿。话题切换 / 关键定义之后,让观众消化 */
  pause: 900,
  /** 1500ms —— 戏剧性停留。punchline 之后 / "想象一下"之类的引导句,留白让观众脑补 */
  dwell: 2000,
} as const;

export type Pace = (typeof PACE)[keyof typeof PACE];
